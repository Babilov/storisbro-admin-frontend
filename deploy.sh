#!/bin/bash

# Скрипт для сборки проекта и пуша в git

# Останавливаем скрипт при ошибке любой команды
set -e

# 1. Сборка фронтенда
echo "▶ Запускаю npm run build..."
npm run build

# 2. Добавляем изменения в git
echo "▶ Добавляю изменения в git..."
git add .

# 3. Создаём коммит
echo "▶ Создаю коммит..."
git commit -m "1"

# 4. Отправляем изменения в репозиторий
echo "▶ Отправляю изменения в удалённый репозиторий..."
git push

echo "✅ Готово!"
